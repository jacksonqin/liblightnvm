#include <nvm.h>
#include <nvm_debug.h>

struct nvm_bbt *nvm_bbt_get(struct nvm_dev *dev, struct nvm_addr addr)
{
	struct nvm_bbt *bbt;

	bbt = malloc(sizeof(*bbt));
	if (!bbt) {
		errno = ENOMEM;
		return NULL;
	}

	bbt->nblks = dev->geo.nblocks * dev->geo.nplanes;
	bbt->blks = malloc(sizeof(*bbt->blks) * bbt->nblks);
	if (!bbt->blks) {
		free(bbt);
		errno = ENOMEM;
		return NULL;
	}

	// TODO: Do the IOCTL

	return bbt;
}

